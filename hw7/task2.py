"""Напишите функцию группового переименования файлов. Она должна:
1) принимать параметр желаемое конечное имя файлов. При переименовании в конце имени добавляется порядковый номер.
2) принимать параметр количество цифр в порядковом номере.
3) принимать параметр расширение исходного файла. Переименование должно работать только для этих файлов внутри каталога.
4) принимать параметр расширение конечного файла.
5) принимать диапазон сохраняемого оригинального имени. Например, для диапазона [3, 6] берутся буквы с 3 по 6
из исходного имени файла. К ним прибавляется желаемое конечное имя, если оно передано.
Далее счётчик файлов и расширение."""

import os


def files_rename(new_name, num_digits, src_ext, dest_ext, name_range=None):
    """
    Функция для группового переименования файлов.
     new_name: желаемое конечное имя файлов
     num_digits: количество цифр в порядковом номере
     src_ext: расширение исходного файла
     dest_ext: расширение конечного файла
     name_range: диапазон сохраняемого оригинального имени (по умолчанию None)
    """

    # Список файлов с нужным расширением
    files = [f for f in os.listdir() if f.endswith(src_ext)]

    # Сортировка списка файлов
    files.sort()

    # Форматируется строка для порядкового номера
    num_format = f"{{:0{num_digits}d}}"

    for i, file in enumerate(files):
        base_name = os.path.splitext(file)[0]
        if name_range:
            base_name = base_name[name_range[0]:name_range[1]]
        new_file_name = f"{base_name}{new_name}{num_format.format(i + 1)}.{dest_ext}"
        os.rename(file, new_file_name)


files_rename('new_name', 5, 'txt', 'dest_ext', [3, 6])
